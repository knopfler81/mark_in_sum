<h2>Amsterdam Ziggo Dome</h2>
<h3>6th June 2015</h3>


<p>
Well we had a speedway to Amsterdam with Lorraine, Lisa and Ingrid. We first dropped our lugguages at the Best Western Hotel and then we went for drinks and food at the Grolsch Cafe before the show. That's the tradition "drinks and food before the show".</br></br>
So we walk to the Zigodom which is a very big venue, I've been told it's 17 000 seats capacity !!</br>
We arrived quite late, so we didn't wait too long till the begining of the show! Once again I had a front row seat  on the right of the stage (Thank you Ingrid, this is my favorite side you know), but a little further this time! My dear sister Lorraine offered me to trade seats because she didn't mind to be little further and she knows how addicted I am to the "perfect seat"! </br></br>

Well tonight's show was rather better than Antwerp's, Dutch atmospher was warmer!
But still too quiet after such Parisian gigs ! Mark brought back "Mighty Man", I was happy to hear to this song again. It really have something special, even if it is not my favorite on the album, I could say that hearing it live,  it sounds better, it has a saoul.</br>

SET LIST</br></br>

01. Broken Bones</br>
02. Corned Beef City</br>
03. Privateering</br>
04. Father And Son</br>
05. Hillfarmers Blues</br>
06. Kingdom Of Gold (With Ruth Moody)</br>
07. Skydiver (With Ruth Moody)</br>
08. I Used To Could (With Nigel Hitchcock)</br>
09. Romeo And Juliet (With Nigel Hitchcock)</br>
10. Sultans Of Swing (Standing Ovation)</br>
11. Mighty Man</br>
12. Postcards From Paraguay</br>
13. Marbletown</br>
14. Speedway At Nazarreth</br>
15. Telegraph Road</br>
16. So Far Away</br>
17. Wherever I Go (With Ruth Moody)</br>
18. Going Home</br></br></br>


As you can see on the set list Mark brought back Speedway at Nazarreth  ! It was soooooo nice to have this song again! I was affraid that he had dropped it forver... When I saw Glen Stagger bringing the Gibson on stage, I immediatly understood that Speedway was back, we all noticed that, we were on fire ! The first row people were yelling of happiness !! What a moment and what an interpretation tonight ! It was back and powerfull !</br></br>

Then came the end of the show, and there I was thinking that I only have one show left and that I will have to leave most of my friends tomorrow... I was soooo happy to share this "small tour" with my sisters, Laurent and other forumers and facebooks guys... 
Crocodiles tears arrived, and the panda bear look too (my black make up). I  couldn't control myself the emotion of the good concert mixed together with the feeling " We're leaving tomorrow"... was just difficult.</br>
We all went for a last drink at the pub again. Hugs and kisses time with all my friends. We wish to meet in a couple of years if Mark continues to tour every two years...</br></br>

Lisa Lorraine and I were staying at the same hotel, we had to take a train to the airport and then the shuttle... But we missed the last train, the tickets machine didn't work well, so I could get a tix but Lorraine and Lisa couldn't. The thing is that I bought the ticket and couldn't be refunded as it was the last train and it was a "today train"... A guy from the train station told us that there was a free bus to the airport but once inside the bus we had to pay. Then Lisa called the hotel to know where we could find the shuttle, she has been told that the  last shuttle was at 11:30pm.... and indeed by this time it was aleardy 12am... So we had to get a taxi. </br>
Before leaving the airport  as there was no rush anymore we decided to have a late snack before going to bed and stopped for excellent paninis !

My last sentence would be to thank all the people involved of making this tour possible for me this year... Sharing room, car-pooling, Xmas presents etc... I won't mention anybody but people will reconize thelselves ;)

</p>
 

