<h2>Antwerp Sportpaleis</h2>

<h3>5th June 2015</h3>

<p>After a long long ride with Laurent, we finally arrived at the meeting point, it took us four hours to reach Antwerp, and almost one hour to reach the Sportpaleis on the busy busy  Antwerp roads ! We stop there to rest and have food and drinks, then we just crossed the street to enter the Sportpaleis!</br>
I got pretty shocked to see that we had to pay 0,40â‚¬ to use the batherooms, it is crazy, we pay for a concert ticket quite an amount, and we pay to pee.... glad I only needed to go once...BTW...</b></br>

Again I got a front row on my favorite side of the stage, between Mike and Richard !
Tonight's show was very good ! But I can say that I missed the "french atmospeher". People here are kinda quiet if you compare to Paris shows!</br>

Mark has again skipped "Speedway At Nazareth" **sigh**, but he was still in a very good mood very smiling. The great intercation between the band members is still at its top  it's a pleasure to see them having fun !</p>


