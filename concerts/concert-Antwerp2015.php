<p>Again it was a good show, even if at the very begining the sound was not that good in my opinion, but it got better quickly!</br>
There was only one standing ovation for "SOS", so as I said Belgium is a quiet audience. I hope tomorrow's dutch audience would be warmer ! </br></br>

My last sentence would be to thank all the people involved of making this tour possible for me this year... Sharing room, car-pooling, Xmas presents etc... I won't mention anybody but people will reconize thelselves ;)
</p>
 


