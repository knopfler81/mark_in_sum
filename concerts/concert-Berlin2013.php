<h2>Berlin O2 Arena </h2>

<h3>10th May 2013</h3>
<p>
D-Day Finally arrived, almost unspoilt! I had to mask all my MK friends from my facebook and avoided forums etc…. I felt so “alone” for a couple of weeks.<br/>
I arrived in the middle of the the afternoon in Berlin, I left home at 7am though… journey was pretty long…btw I had a good reason !</p>
<p>I first met Ingrid at Plus Hotel-Hostel, which was pretty close to the O2 Arena, and just next to the rest of the Berlin wall. Although I recommend this Hotel, it’s pretty cheap, clean, and comfortable. We went to have a late lunch with Maarten and Martina, Jeroen, Isaac and Martina joined us for a drink . Afterwards we went to the venue.</p>
<p>A big venue 15 000 seats I guess( like The POPBercy)… We were in the front row but on the very left. 
Do you know the feeling when “feelin’ so good”starts ??? Heart starts beating fast, shivers everywhere and then a big man arrives on stage with the union jack jacket ! Yeahh PC comes and introducesHere MK every night !<br/><br/>
And then all the guys come around and the show starts !!! WII for an opener is indeed a good song but not as surprising as Border River was! But I like it anyway ! And then many surprises in a row ! On this tour you get more surprises every night I guess !</p>
</p>
 
