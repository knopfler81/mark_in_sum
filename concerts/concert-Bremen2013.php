<h2>Bremen</h2>


<h3>11th May, </br>A speedway to Bremen ! You will understand!</h3>

<p>Igrid and I had a nice cheese breakfast on a terrace in Berlin. It was sunny and pretty warm! We really enjoyed it ! We decided to open the 308CC roof for our speedway to Bremen... 300 meters further the police stopped us for 53km/h... *sigh* we didn’t see the 30 sign... Anyway...speed limits are indeed in town and thank God not everywhere on the highway. In Germany, when you reach 210km/h you really feel *FREEDOM* Ingrid is a great driver and really careful!</p>
<p>We arrived at around 3:30pm in Bremen. We had a very nice hotel room that we shared with Maarten.
The venue was 2km or so from the venue, so we walked there, stopping in a Bavarian restaurant with traditional music, traditionally dressed waiters and traditional food which was YUMMY ! I found out that a Bavarian Pizza is actually a flammekueche !</p>
<p>It was raining cats and dogs when we left for the venue, so we ran until there when we met Martina who was waiting for us for a little while! We received some working passes and stood in the main entrance for some flyering. I’m still not really sure what this flyer really was for it was all written in German! But It was for some pictures of the Privateering concert. It was pretty funny because no one could guess that I was French and not a speaker of German... So each time I was asked « Was its das»... «Sorry I don’t speak German was the reply».</p>

<p>Then came show time...We had 6th row seats, pretty far but actually the sound was very good. The concert was brilliant and MK was at his best! Good mood again happy to play in this small venue! We had great a surprise again with I Dug Up A Diamond... It’s not that bad to be a little further back because then you see the stage differently ! Lights, guys, etc...</p>
<p>Only one thing was bothering Ingrid and me... It’s a MUST to rush to the stage for Telegraph Road !! I did something I would never have dared to do if i wasn’t with Ingrid that evening... When TR started we left our seats and went to the ladies yes we did !!... It took us maybe 3 minutes and so like smart and polite girls we waited next to the audience doors so as not to disturb our seat neighbours... Security was watching us closely... When TR ended, we indeed rushed to the stage, Ingrid managed to avoid the security lady who actually caught me because of missing Ingrid...ARG !!...I don’t know how I managed to escape from this big lady !! But we had our very front row for the encores ! And this is the best feeling ever! The closeness with the man and his band !
I was surprised and little disappointed not to hear BIA... ah well...</p>
<p>After this wonderful show, we met up again with Isaac, Jeroen, Maarten, and Ingrid for a last drink and walked back to our hotel saying goodbye until the next show, which is Brighton for me !!
</p>
 


