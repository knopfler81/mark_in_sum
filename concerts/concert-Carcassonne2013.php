<h2>Carcassonne Jean Deschamps Theatre</h2>
<h3>24th July 2013</h3>

<p>The end of a perfect tour in Carcassonne</p>

<p>After a four hour journey, I finally arrived at Carcassonne’s train station where Jeroen was waiting for me. As we were staying in the same hotel he offered to pick me up there, in order to show me the way.  After a very short break in my room I met up again with him and Isaac and we went for  lunch !  We went in the centre of Carcassonne where there is nothing nice to see, but we had good food ! Another break at the hotel was needed for me, I had a nap for maybe one hour, while guys stayed in there room writing their blog !<br/>
We walked to the Medieval City later in the afternoon, the concert was happening there and this old city is very beautiful  but there were SO many people! We agreed that it was weird to see such old building with so modern shops and restaurants... But it’s like it is !  We split up for a while, because coincidentally a friend of mine (who used to be my neighbour and had moved to the south of France had texted me a picture earlier in the morning to show me that he was in Carcassonne Medival city ! So we decided to meet up and have a drink! I was really nice seeing him again!
</p>
<p>I sarted to queue in front of the venue’s gate when I realised that the  boxoffice this time was not in the venue but behind me, Am I blond or what? No just very tired...!! So I picked up my tix and found out that I had an assigned seat (I didn’t know what I would have had, because this reservation was a last minute one...). I got lucky to have the golden circle area ! So I didn’t have queue anymore as I had assigned seat. So I met up again with the boys and we went for another drink & snack.<br/>
We arrived at the theater pretty late, and there I was happy to meet again Val and her hubby, the two lovely american sisters, and indeed a bunch of other folks!<br/>
As I really didn’t know where I would be seated Isaac and Jeroen told me that in case I have a very bad seat I could sit between them ! But I got Lucky my seat was just perfect ! A 6th row but as the theater was very small it was probably the best seat «in the house».  When the man and his lovely band came on stage I stood up from my 6th row... but no one followed me... I felt lonnely for 10 seconds btw ! <br/>
The stage was installed just head on the castle. The back ground was just amazing with the beautiful lights on it ! It was the first time of the tour that I could really picture the WHOLE stage with every musicians! Incredible view !<br/><</p>

<p>I was happy to hear Haul Away for my last show, it’s such a beautiful song! Unfortunaltey Mark dropped Speedway At Nazareth. Actually I had planned with the boys that at the end of this one I’ll join them and sit on their lap... When I saw that MK was getting the National  instead of the Gibson Les Paul, I saw that Isaac was looking for me... and I knew it was time for me to go downstairs and join them... I sat on their lap during TR, I don’t really know what the musicians thought of this but some of them laughed.. well... Jealous? ;)
Right after TR we stood, so Isaac and Jeroen were relieved of my weight  !! The encore were really enjoyable at the very front for my very last show on this tour! <br/><br/>
I really want to thank Jeroen and Isaac for their kindness ! And all the people involved in helping me get there. 
</p>
 


