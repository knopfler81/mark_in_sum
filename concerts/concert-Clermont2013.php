<h2>Clermont Ferrand Zenith </h2>
<h3>29th June 2013</h3>
         
<p>Here we are ! The day of Clermont’s show ! </p>

<p>I’ve really looked forward to this one because my parents had also planned to come with me! 
We’ve finallllllllly arrived in Clermont, no it wasn’t that a long drive, but we had to face to a little car trouble... The Peugeot couldn’t speed anymore because of (we think) a dead turbo... This was really annyoning ! But actually dad was provident, we left early and arrived at 6pm at Le Zenith ( This very ugly green place was built circular to look like the volcanos around... Trust me that looks more like a bunker or I don’t know what, than a volcano...).</p>

<p>This year my parents finally met my «dutch sister» Ingrid and a bunch of other die hard fans Jeroen, Isaac, and AFMK guys !Ingrid did a very good job when she bought our tix, we had a very front row just a little on the left.</p>

<p>As for all the concerts in France this week we had the pleasure to have Ruth Moody as opener ! This lady from Canada is just wonderful and so is her band ! Her music grew on me from the very first time I saw her (London RAH). I cant’ say that she warmed up the audience, but she made us sing « life is long love, life is long...» and so on... The whole audience was attentive to her!
</p>
<p>
And then after the break the same big guy came on stage to introduce «MARK KNOPFLER» ! It’s always exciting to welcome Mark on stage, we were on fire !
The set list didn't change a lot this week... Not much surprise...
It seems that Sultans’of swing is definitely out. But we got lucky to have the wonderful «Kingdom Of Gold» ! <br/>
This tune is just « DEEP SIGH» you must hear it live to understand ! The ending solo is amazing on this beautiful new blue Pensa ! MK defiantly still has gold in his fingers !
</p>
<p>Before the show we (fans) agreed on what song we’ll rush to the stage at... Usually it’s right after Speedway but MK dropped it the night before... So just in case we decided to go to the stage after Marbletown! <br/>
I think I have prepared and trained my parents to rush for months...Actually, they did rock ! My Dad ran that fast that I didn't see him arrive before me ! <br/>
Jeroen (another Dutch guy) really acted like a gentleman and left his place to let the three of us together for the Encores! I couldn't expect anything better! Being at my guitar hero concert with the two persons I love the most on this earth woowww ! It was so burning for me to share this !
But every good thing must end... It was time to go back home. This time I left with Ingrid who was staying home for the night! Yeahh we’re going to Dijon the day after !!
</p>
 


