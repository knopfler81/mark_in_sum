<h2>Dijon Zenith</h2>



<h3>30th June 2013</h3>

<p>Sister Ingrid stayed home for the night, we had a quick lunch before sleeping and a quick breakfast in the morning.  She left first to pick up the boys ( Isaac and Jeroen) at Lyon train ‘station! I left on my own a little later and drove straight to Dijon, 3 hours without stooping ( something that I will never do again, very tiring). I arrived very early at Le Zenith and met Elian and Romain on the parking lot. After a three minutes chat they left to try to meet Mark... I followed them... And «Sigh» we missed him 2 minutes too late...Btw I’m ok with this I’m not «Rudigering» anymore... Sure I’d be happy to talk to him again, but I already got this lucky many times...So I’ve survived !</br>
After this misfortune, we drove in to Dijon to find «food», it was Sunday and the only open «restaurant» was Mc Donalds but... It’s ok ! I personally really had a pleasant moment talking about Knopfler’s music with the two guys! We agreed that we’re really lucky to have him touring again and again!!</p>

<p>I waited a long time before entering in the venue and have met so many people outside ( even Marco was here !!!)! Time to chat and to collect the tix... Once again we had the very front row, a little more in the middle than the day before ! Pretty much the same gig as the evening before , less intense for me, because my family wasn't there and I was very tired! But it was still a very good performance...<br/>
Set list was almost the same, no surprise...But as I have said before... even if there is no surprise, even if the show is very similar, the feelings are different ! The people you’re with the general atmosphere count a lot, and indeed the behaviour of the musicians !</p>

<p>Tonight I had to say good bye to my pals before the gig, because I really had to run away as quick as possible at the end of the show to avoid the traffic jam! I did a good job because the gig ended at 11:15pm and I was on the road at 11:30pm ! It was a long long drive but I arrived home safely. ( Oh and I think I’m gonna have my very first speed camera ticket...).
</p>
 


