<h2>London Royal Albert Hall</h2>
<h3>2nd June 2005</h3>

<p>Thursday June the 2nd, I took the Plane to London’s Royal Albert Hall to applaud the best guitarist in the world, alone in the end... but that's great !!</p>
<p>I was sitting on the 2nd row, it was so wonderful to be so close to Mark and his musicians. My eyes and my ears really enjoyed every minute of it
when Mark arrived on stage I stood up to applaude him and yell wooooow Mark.... but i saw that was alone standing up... OOPS</p>
<p>English people are more reserved!
Sultans of Swing was really really great, so, standing ovation!! A while after Telegraph road made us totally crazy we stood up again but did not sit down again! Then I jumped over the chair in front of me and stood with my elbows on the stage.. I was so close !!!!!
Mark came so near us that we could have touched him...  (Don’t touch MK !!)
A really great memory in my mind for ever.</p>

