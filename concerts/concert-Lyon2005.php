<h2>Lyon Tony Garnier's Hall</h2>

<h3>5th April 2005</h3>

<p>Finally the 5th of april 2005 came, the day I had been waiting for for years! (After the 2003 cancelled tour.)<br/>
I arrived very early hoping to meet Mark and his musicians. But I missed them... I just saw from  far Luke Brighty  & Wiliam Toplay...the support  band. I waited 7 hours in front of the Tony Garnier's hall before to see the man on the stage!
I was so glad to see him again after  four years of waiting, I jumped up and down  in the audience because I was so happy .So much emotion, his new album Shangri La is a pearl
Obviously I left the hall  full with  joy !! And I couldn't wait for the three following gigs I had booked.</p>
 


