<h2>Nimes Arena</h2>


<h3>9th July 2013</h3>

<p>The «Wedding» anniversary gig’s !!</p>

<p>Today was the 17th anniversary of the very first time I saw Mark ! Time flies, (check out my very first review...).<br/>
I left work at noon, I had an appointment in Givors with Adrien who drove me to Nîmes! We had a short break at Montelimar rest area, time to feed ourselves and then left again for the Arenas ! 
When we arrived a couple of frenchies friends were waiting for Mark with their guitars expecting autographs... I decided to stay with them, there was nothing else to do except go for a drink... But the opportunity to cross the path with Mark was likely... So I restarted «Rudigering» Awww I must see a doctor for this hehe !<br/>
Mark arrived in the usual big Range Rover and stopped. He didn't come to us but Pete did to take the three beautiful guitars to Mark who signed them... and that was it ! No chance for us to have anything...But I don’t mind ! I’m really happy for Elian, Romain and his brother ! They deserved their autograph after such a long time !!! <br/>
I couldn't name everybody but I was really happy to meet the same old folks again and again, and the new faces indeed !</p>
<p>After a couple of drinks we went inside the arena a little late, which was already crowded ! Seats were assigned and everyone was happy with the seat I had been able to book! Very front row with opera numbering ! This really rocked !<br/>
Bap Kennedy was the opener of this show ! I really really love him ! A pity that the audience didn't pay much attention to him... He’s very talented though!
</p>
<p>Right after Bap, a couple of guys and me tried to start a mexican wave! It took us five minutes to warm up the whole arena but we did it ! It was awesome and so beautiful to see all those people following us !! OOOOOOOOOLÉ !!!! I hope that there will but some video shots !!
The big man in his Union Jack jacket came on the stages to introduce Mark, and.... So it started ! We had a little sound trouble during What It Is but it was quickly solved. I don’t know what happened tonight but there were also problems with the lights. It was quite a mess and Mark did joke with this... He said something like « Oh we have Christmas lights tonight».
Anyway it didn't affect the performance nor the sound ! The guys were in very good mood ( like every night). </p>
<p>No surprise at all in the set list, still no SOS (but I survive) and phewww Speedway is back ! And tonight Mark really played it ! I mean it was BRILLIANT probably his best performance for ages on this song ! Indeed right after Speedway we stood and rush to the stage and stayed standing for the encores ! This year we could stand, no one moaned ! The arena was really warm ! Telegraph Road was also really brilliant, same feelings as Speedway...</p>
<p>The encores were «classical» but even So Far Away was more amazing than usual ! Maybe the old arena inspired Mark !<br/>
And it was time to leave... I had time for a short review with other fans who had the same feelings! WHAT A GREAT NIGHT !! We were all blown away!!
</p>
<p>Adrien and me left earlier than everybody as we had to go back to the «North» !
Special thanks to Adrien for his safe driving !
Till the next time,</br>

So long...

</p>
 


