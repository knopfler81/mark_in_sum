<h2>Paris Bercy </h2>

<h3>9th June 2010</h3>

<p>Mark Knopfler played that night in Paris Bercy, which is a huge venue 12 000 seats! I guess it was sold out!</p>

<p>I arrived in Paris by train around 3:00pm, checked in my hotel, had a quick snack, a little nap, a shower and then I went straight to the big venue (under the rain)!
</p>

<p>I met Isaac again, Jean Yves, and few people from the french association AFMK.</p>

<p>We had to waited  in a line-up to pick up our pre-ordered tix at gate 35, it was pretty long and badly organized... And then we had to go to another gate to enter in the venue.</p>

<p>
Once again I got lucky with a very front row in the middle, thanks to my friend J-Y who purshased my tix ( I couldn’t have done it when the pre-orders strated as I was at work with out internet access, oh well so slow ... and not allowed during the calls).</p>


<p>I bought my lithographies (one for me one for Laurent) before the concert because I was scared that they would all have been sold at the end of the concert.... Taking care of them during the gig was difficult, but I’ve almost managed!</p>


</p>MK started the set list with that wonderful opener Border Reiver ! He was in very good mood, quite talkative and happy to be in Paris ! He still plays sitted but as I said in my London review it doesn’t affect his playing. He confirmed us that his back is better and that he wanna keep the sitted position because he likes it !</p>


<p>I was very excited during the  concert so happy to see those men on stage again ! They’re all so wonderful musicians!</p>


<p>I also would like to share here on of my strongest moment in the concert.</p>


<p>It was when I heard the very first notes of A Night In Summer Long Ago. So tears simply arrived, impossible to control myself, they were here and ran during the whole song... It was because of one of my best memories with my grand mother "Juliette". She really loved MK since I've attended my very first show in Lyon Fourvière 1996 (I was 15, and was always mentioning MK/DS music's )... And then she used to ask me very often : " Nelly, please I want to listen to the guy you saw in Lyon !! " And THAT song was really her favorite. She didn't know any english, so that weren't the lyrics... She just loved the music, I still can see the smile she had on her face while listening... She passed away a year after... And this song as now a meaning for me...
BTW when the song ended I was still in tears, then they played Marbletwon and tears were still here...it was hard to come back to reality!</p>

<p>And here came the troubles (not serious but stressing lol). People were very hot in Paris and instead of standing up at the end of TR they stood up at the end of Marbletown, I wasn’t prepared at all... So I stood up quickly took my lithographies my bag with me and ran straight to the front, (few elbows chocs) and noticed that I only took one of the litho.... so I got back to my seat, picked up the litho and go back to the front... and there again I ‘ve noticed that I’ve lost my camera! So I got back to my seat again and tried to find it back... impossible I was madd. So I got back to the front (well with 3 or 4 tall guys in front of me...grrr). Later Isacc saved my life with finding my camera back ! And as a gentleman at the end of Telegraph Road he proposed me to switch off places then I was closer to the stage again! (Thank you very much Isaac)!</p>

<p>During the encore a 6 years old little girl gave a drawing to Matt Rolling for Mark ! I was very cute and I guess that Mark appreciated it because at the end of the concert  Mark asked Danny for his sticks and gave them to the little girl !! That was very cute ! ( Next time I’ll deguise myself as a little girl... lol).</p>


<p>That was a wonderful concert of course ! This is the one I could never forget because of ANISLA.
</p>
