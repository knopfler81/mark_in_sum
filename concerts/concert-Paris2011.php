<h2>Palais Omnisports Paris Bercy</h2>


<h3>17th October 2011</h3>


<p>Very surprising, Mark Knopfler and Bob Dylan on the same tour, but for two separated shows.
That’s the reason why I attended only that gig this year... I knew that Knopfler shows would be shorter than what we’re used to have. And I’m not a  Bob Dylan fan, even if I totally respect the artist he used to be in his early years.
</p>
<p>So I arrived at Paris Gare De Lyon early afternoon, went straight to my hotel and had a quick snack. I was tired because of the trip so I had a short nap before meeting Isaac around the venue.
While waiting for Isaac I walked a litte around big Bercy and found out few «Rudïger» waiting for Mark. I prefered leaving... To many people  were here already and I think Mark doesn’t really  like that. Finally I met up with Isaac it was great to see him again.<br/><br/>
</p>
<p>I was really desapointed with my seat assignment... Even if it was a front row it was on the very left of the stage.  I think I would have prefered a third row in the middle!! 
Before the show I teased Isaac for stealing his seat (which was row#1 in front of Mark). He almost wanted to swich (what I didn’t accept because it was HIS assignment) and then he proposed me to sit on his laps. I first though it was a joke but Isaac promised to behave himself so I accepted and finally sat on the chair between his legs ! It was pretty funny when the band saw us sharing the seat.</p>

<p>About the show now: **WARNING SPOILERS AHEAD</p>


<p>So I can tell you that I of course loved  Mark's concert. 
The atmospher for MK wasn't that warm in Big Bercy (It usually is for MK gigs), I could feel that most of the people were here for Bob Dylan's show.
</p>
<p>My feelings with the songs:<p>

<p>What It Is
I thought I was spoiled with Why Aye Man as opener... So I wasn't 
<br/><br/>
Cleaning My Gun  
I didn't expect this one at all ! A pretty good surprise and a very very good and powerful version
<br/><br/>
Sailing To Philaldelphia
Just lovely as Always
<br/><br/>
Hill Farmer Blues
POWERFUL INCREDIBLE it will always give me SHIVERS
<br/><br/>
Privateering
Big Surprise again, I heard there were  new songs played on the this tour. I liked it very much ! 
<br/><br/>
Song For Sonny Liston
Blue Les Paul? Oh Well why not...... OF course it was nice but I think I prefer it from the older tours
<br/><br/>
Done With Bonaparte
Not a surprise, Many people think it should be dropped...This is a song I used not to like before 2010... Now, i love it( better later than never).
<br/><br/>
A night in summer long ago
For those who know my feeling for this song.... No! I didn't cry, I was surprised but it wasn't the same "shock" as last year Maybe I was prepared..( He payed it also in Paris in 2010).
<br/><br/>
Marbletown & Speedway At Nazareth
Again  great  performances !
<br/><br/>
So Far Away 
The good bye song... I may dare to say... i'm a  little tired of this one.. Would have prefered another.
<br/><br/>
I'm very positive on this concert, yes it was short but I knew it i didn't expect much! I had great time with MK and his Band...
<br/><br/>
</p>
<p>About Bob Dylan...Well  
Several people warned me about the loud sound and Bob's "bad" voice...
I recall bad memories of my first Bob concert's in 2003... But i felt like staying to "give him another chance".
When the concert started everybody rush to the stage gates it was a complet mess... 
As I was seated on the very left of Big Bercy (my official seat). I couldn't see anything... 
I left after 4 songs... I don't wanna tell bad things about Bob, I respect the artist... but i'm sorry i really didn't like.
</p>
