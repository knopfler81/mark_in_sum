<h2>Paris Bercy </h2>
<h3>26th June 2013</h3>

<p>After an overbooked morning at work, I’ve finally left my office in time to catch my trains from St Etienne to Paris.  I arrived in Paris in the middle of the afternoon , checked in, in my «luxurious» hotel (the walls so think that I heard my room neighbour snoring...).
</p>
<p>Adrien from the AFMK was in a hotel close to mine, so we walked together to the venue. On our way we met some folks of the AFMK and went to the Brasserie for a drink (or two...) and an early dinner (for me)... I received a phone call from a friend who was queueing to pick up the tickets, he warned us that the organisation was a mess and that it was taking ages to get the tix... We immediately went to the box office and YES it was a mess and YES the queue was looooong...As I didn’t have to queue I went talking with my friends Laurent, Isaac and Jeroen who had almost finished queuing ( I think something like 50min). Then a naughty idea came to me... I managed to get the two friends who had to queue to have a chat with Isaac & Co...(you see?).
</p>
<p>
Well now with the tix in the pocket we entered in Big Bercy, we really didn’t know what our seat numbers could be... And actually the surprise was wonderful ! Straight in the middle ! What Else ??
</p>
<p>As in London, Ruth Moody was the opening supportthe , I don’t know why but more I see her more I like her! She’s a really pleasant young lady with great chaps in her band !
</p>
<p>After a 30 min break to set up the stage, MK finally arrived in a very good mood as every night on this tour !  It was also for me THE night to learn how to set my new digital camera «sigh»...You’ll see the pics... The set list hasn’t changed a lot for a couple of weeks now... I’m not complaining I love the way it is ! But a set with out Sultans of Swing is kinda «little less exciting...» That’s only my feeling and maybe it’s the very first time I admit I’m little disappointed but I’ll survive! And INDEED it’s Mark’s choice and I respect that !!
</p>

<p>As with every MK concert we prepare (pack our stuff) to rush to the stage at the end of Telegraph Road... But In France it’s different we are used to rush after the song before, Speedway at Nazareth ; and this year something silly happened! A guy from nowhere first rushed to the stage alone, stood in front of us ! I think it took us a couple of seconds to react that IT WAS TIME to follow him!
</p>

<p>When we were at the very front of the stage everything changed ! The atmosphere, the comfort, the excitement, the heating... I’ve started feeling bad during TR but I managed to focus on MK thinking «no Nelly don’t fall not now please» and it worked !
</p>
<p>Those wonderful shows are always too short, we wish they would never end.... Till the next one... Clermont Ferrand ( two days to wait) !
Anyway after this brilliant show I went with the folks of the AFMK for a last drink to the Brasserie des spectacles and walk back to my hotel with Isaac as body guard ( he was in a hotel close to mine too)!
</p>
 


