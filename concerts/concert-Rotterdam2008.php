<h2>One night in Rotterdam</h2>

<h3>31st March 2008</h3>

<p>My last night in Holland was even better than the two previous one, it seems that they get better and better every night. 
That night the concert was seated, I have been VERY lucky to have a front row again, but this time on the left... <br/>
Maybe the best place, because there is John McCusker and Mark seems to enjoy playing with him a lot and always smiles to him! Very pleasant to watch. The security was very strict. Two men were seated back to the stage looking at us... <br/>When Speedway At Nazareth ended they left, and we immediately ran to the gate! That was great I was so close to Mark, see the pics ( I'm very happy with their quality). It's hard for me to explain my feelings, not enough words to say how much I was happy with this trip to Holland with my good friends !Too bad that you are so far away from me.I'll never forget this great time with you all , Mark and the band ! (And Ingrid's hospitality of course).
</p>
 


