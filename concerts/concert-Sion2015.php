<h3>17th July 2015</h3>


<p> Sion is gonna stay in my memorie for ever for diverse reasons:</p>
<p>My two wonderful Dutch and Swiss friends made me an incredible surprise ! Ingrid and me drove to Annemasse aerodrome where Man On A Track came and pick up us for a flight to Sion ! I was really impressed to fly in such a small plane Robin 3000 but I really felt safe MOAT is very careful. Ingrid has also piloted ! She was born to become a pilot she did it very well. We actually landed where the band landed but unfortunatenly we missed them.</p>

<p> We went to the hotel for a quick shower and then went straight to the venue. It seemed to be more organized than Saint Julien as there were lanes for Fans entrance, e-tickets entrance etc... Actually the security staff didn't know that we fans had early entries and it was a little mess to make them understand that we were supposed to come in first btw I won't detail all, as it got solved quickly.</p>
<p> So we could enter in the venue and kindely walk to the front row, Swiss audience is the most respectful audience of Europe I beleive, no one wants to steal your place, while your waiting you can go and come back easly. They make space so you can breath ! I had to visit the medical staff again, as my knee was hurting and I need to cover it for the night to make it safe. Medical staff was very cool and funny! Then I could get back easly to my front row, Swiss people behave. Also a little cool rain has refreshed us, it was sooooo appreciable.</p>
<p> Sonny Landreth was opening the act here too and it was much better than the day before! The sound wasn't to loud and I really could enjoy and had fun.</p>
<p> Mark took the stage around 21:15, the show was very good, all the members were in top mood! The audience was less warm than Saint Julien's (well that's difficult to be warmer), but still very good and friendly.</br>
During Sultans Of Swing Mark had troubles and yelled something like "I'm done", he had sound trouble with his guitar and his amp but it was quickly solved. So this SOS is kinda collector because Mark played it longer and it was perfectly done.</br>
As expected  "Your Latst Trick" was also played again, this track is just magical !!</br>
This was my last gig of Tracker and I was about not to come I had planed it for the pre sales and finally decided to not make it and changed my mind again.... It was almost a last minute decision ! I don't regret at all </p>

