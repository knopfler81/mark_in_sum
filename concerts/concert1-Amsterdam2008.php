<h2> Amsterdam Heineken Music Hall Kick Off Gig</h2>


<h3>29th March 2008 </h3>


<p>My first time in Holland was amazing! I met wonderful people, Ingrid, Nigel, Maarten, Philipp, Waldo, Noella, Fieneke and her Hubby, Val and her Hubby, Martina, Mathias... We first met in the Grolsch Cinecafe, and queued from around 16:00, there were already a few people waiting... the time went quite fast until the doors opened... so many things to talk about with friends. The weather was rather good this saturday so it was OK to be standing outside. We were counting every minute ... And then finally the security opened the door and we ran like mad people straight to the front to get the best spot ! We were standing in the very front on the right in front of Richard... At last 8pm came and the concert started immediately !</p>

<p>
I think Mark Knopfler made a great effort for his fans! He actually took away two well known songs from the set list ( Walk Of Life and Money For Nothing) and played more from his wonderful solo career. I was a bit disappointed that he didn't perform more than two songs from his new album, Kill To Get Crimson (I really think that Punish The Monkey would have rocked a lot) but of course very very happy to hear some of The Ragpickers Dreams songs ( finally we had our 2003 show ;-) the cancelled one). The concert lasted around 2 hours, that's always short for us. But we were attending another one the day after !</p>

