<h2>London Royal Albert Hall</h2>



<h3>25th may 2008</h3>


<p>My second time at the Royal Albert Hall, one of the most beautiful venue in the world I guess! It was eventually a long trip to arrive in London. </br>
My plane was delayed for 4 loooooooong hours, but i've been lucky to meet a couple of french people who spent time talking with me. Safe arrived at Marlylbone station where i first met my fellows Ingrid and Nigel! We were staying in the same hotel. </br>
We met many other fellows in a pub,in Gloucester road before to go to the concert. So we met Emily from Boston wooow ! Martina, Philip, Jean Yves, Jeroen... and some names that I've'forgotten I'm affraid. Then we walked to the Royal Albert Hall and what a Surprise for Nigel Ingrid and me, we were supposed to be sitted at 4th row on the very left.</br>
And actually the 4th row is a 2nd row ! So we were even closed to the stage : what an amazing surprise. I was like a little girl, so happy. Unfortunately not all was perfect... and the most imprtant in a concert was a nightmare this evening. The front speakers didn't work well. So sometime we had Mark's voice and guitar's sometime not... and vice versa.... </br>
We tried to wave the problem to the musicians. I think Matt understood because when he was off to the stage and ingeneer came to check it, but nothing better happened.... Too bad ! Really desapointed of the sound. I couldn't really feel the performance. </br>
But at least it was nice to be in RAH again. Just after Telegraph Road we could stand up and run to the front of the stage wich is very low without gate! So we were very very close to the band. See my picures, I'm quite happy they are even better from the very front. The set list didn't change at all, we expected for a special guest but noone at the horizon, "sigh"
</p>
 


