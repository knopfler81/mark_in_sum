<h2>London Royal Albert Hall</h2>

<h3>30th May 2010</h3>

<p>
Mark Knopfler has been playing 6 sold-out shows in a row at the Royal Albert Hall in London.<br/>

Here’s my review of the first two, which I have attended.<br/>


We first had a fan meeting at The Stanhope Arms on Gloucester Road. I really enjoyed meeting all those wonderful people again (and the new faces of course)!!<br/>

I missed that atmosphere which is very special with fans. Being with people who have the same hobby, who feel the same as you do towards the music, is magical... Crazy fools who travel to see the man and his beautiful band !<br/>

After a few drinks and a little snack we walked to the magical venue - the RAH ! - then waited a bit in a line-up to pick up our pre-ordered tix (I booked mine last June).<br/> <br/> 

So it was the time to enter the hall and again that strong emotion of being here came to me. Shivers, breath cut...<br/>

I got very lucky with my seats ! Very first row for both nights ( very left on the first night and in the middle right for the 2nd). The view and the sound were perfect! ( better than two years ago at almost the same seat, one row behind).<br/>

I’m very glad because I tried to stay unspoiled from the set list. I wanted to experience the surprise in the gig. So I avoided almost everything since the tour started ( forums, diaries, blogs... I even created a temporary account on Facebook to avoid naughty people who could have spoilt me...).<br/>

Anyway... In my dreams Border Reiver was the perfect show opener... and guess what ??<br/>

Yup ! I had shivers in my whole body and was about to pass out when I heard the flute ! OMG I was in heaven within 3 seconds already!<br/>


Mark is still playing seated, as he has been since he had that trapped nerve !<br/>

I was really worried for him and was scared that the tour might be suspended... According to Mark's explanation during the show, he feels better now.<br/>

His playing is wonderful and not affected by the position, I may say that he plays better (seems more relaxed... it was funny to see him swivelling on his chair, looking at his band).<br/>


As I didn’t know the set list at all ( okay only one song...) I really had great surprises with Coyote and Prairie Wedding, I was also delighted to hear Hill Farmer's Blues again, which is one of my favourite songs.<br/>


At a Mark Knopfler concert, if there is one thing I’m waiting for (in a seated venue), it is the end of Telegraph Road to rush at the front of the stage ! Standing closer to the marvellous band... Tickle their calves...LOL. Being there, I wished the concert would last for another 2 hours !!!<br/>


That is what I have to say about my trip to London, for now! More to come soon.... maybe when I’ll get down from my cozy little cloud !!


</p>
 


