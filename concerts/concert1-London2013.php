<h2>London Royal Albert Hall 1st night</h2>

<h3>Sunday 26th day off. </h3>

<p> left Brighton by train with Martina and Maarten after a wonderful and real English Breakfast near the sea side, how fantastic it was to sit in the sun, it was quite cold though but sunny, I’ve really enjoyed it. <br/>
The journey went well and pretty quick, something like one hour to reach London. We all split up at Victoria to reach our own place!
I was staying at Copthorne Tara with Ingrid who actually booked our room. I arrived first, and couldn't check in because the receptionist didn't believe me that I was sharing the room with Ingrid even with the reservation print! I had to call Ingrid and pass her to the receptionist to confirm that we were really sharing! *sigh* I was asked to show my passport ! Woow Is this England ?
</p>
<p>Ingrid arrived an hour after me and it seems that I felt asleep! I was actually exhausted ! I was badly sick the week before and I was still coughing a lot ! That was really tiring! Well well let skip to the concert day !</p>

<h3>Monday 27th Concert Day !</h3>

<p>Ingrid and I woke up little late and went to Portobello Road after a little snack with Jeroen and Rob Henk! We both had a drink on a terrace and I did some shopping for my nephews.<br/>
Most of fans were staying the same hotel so that was easy to find each other. Back from Portobello we met up again with the guys and decided to have Thai food, Isaac, Jeroen, Philipp and Werner joined us.  Before the show we met other guys at the Stanhope Arms Pub (on Gloucester road) I was really delighted to finally meet John and his wife ! A lovely couple ! Also happy to meet Val and her husband, Marie, Emily, Valicia, Matt, Nigel .... And the poeple I forget to mention ^^ We all walked to the venue, picked up our tix and sat in this amazing theatre waiting for Mark ! Very front row again seat 21 for me! In front of Mark ( just a little on the right).<br/>
Ruth moody was the opener support...Not really my cup of tea but she has a pretty nice band!  <br/><br/>Then came Mr Crockfrord to introduce THE show ! Mark and his band were oh you know! In a very good mood happy to be here and they really played good music! Sultans of swing was back on the set list and I think this really warmed up the audience ! We did a couple of standing ovation ! « Oh you’re very sweet»!</p>
<p> Ruth came along on stage for a couple of songs , also Nigel Hitchcock with his wonderful sax !  We all agreed that tonight's show was the best so far! Really !  We all walked back to our hotel for a last drink all together and had lots of fun chatting! I was so lucky to meet all these wonderful guys ! I really looked forward to the next show, London 2nd night !!
</p>
 


