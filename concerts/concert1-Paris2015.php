<h2>Paris Le Zenith</h2>
<h3>2nd June 2015</h3>


<p>THE BEST show so far !</br></br>
Once again, my dutch sister Ingrid got me the "best sit" in the house, on the right of the stage, just in front of Richard, Guy, and Mike !</br>
Tonight was ELECTRIC. When Paul Crockford arrived on stage to introduce Mark, we all stood up, until the end of the opening song "Broken Bones"! Clapping in our hands, dancing, woowowing (I've just invented this word...).</br>
Mark was absolutly perfect tonight ! (Nice shirt).</br></br>
Actually there were several standing ovations... After Romeo and Juliet we launched the traditional french "Oé Oé Oé", Mark played the game and started to put some music on it while we were singing the "Oé Oé Oé". Then Sultans Of Swing started, we were on fire , we all stayed standed... </br>
Some people from the back had arrived slowly to shot some pictures etc... It was getting hotter and dangerous to get our front row stolen ! So I've pushed my bag to the front and stept forward to the security gate, for I think my earliest rush ever !! Security guys tried to kick us back to our seats but it was impossible to go back... The crow had leaked from everywhere in the house !</br></br>
I was happy to stand for almost half the concert, but I think it's a pity and feel sorry for people who got their front row stolen by people from the back as we all payed extra money to book such seats!</p>

<p>Btw this concert was to me the best on this tour ! I really wonder how it's gonna be tomorrow... I'm sure the security will be "stronger"... Lets wait and see !</br></br>
My last sentence would be to thank all the people involved of making this tour possible for me this year... Sharing room, car-pooling, Xmas presents etc... I won't mention anybody but people will reconize themselves ;)
</p>
 


