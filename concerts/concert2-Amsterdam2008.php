<h2>Second night in Amsterdam</h2>


<h3>30th March 2008</h3>

<p>Second concert in Amsterdam! Better than the day before MK and the band were very brilliant! They looked happy to be on stage! MK smiled a lot! The Newbie John McCusker is a really amazing musician, it seems that Mark really enjoys playing with him. I was also on the right tonight because I though that MK was much turned to his left to Richard... but it's finally not true... Folks told me that he was great also from the left. They Switched Postcards from Paraguay with Devil Baby! Another one from The Ragipickers Dreams Album ! Fantastic song ! Slower than the studio version but, even better in live.</p>
<p>Once again the concert lasted around 2 hours, so short for hard die fans like us! But we still got some gigs left hopefully...And the tour is just starting! As it was their first show MK's voice is really perfect, unfortunately it will become less good with time... because of tiredness, I guess but the guitar playing will be rather better in few times! As they are better each night ! So I guess that Paris and London will be marvellous!
</p>
 


