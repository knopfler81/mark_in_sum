<h2>London Royal Albert Hall</h2>
<h3>26th may 2008</h3> 

<p>Once again in the most beautiful venue in the world. Same seat as the night before with my fellows Ingrid and Nigel. That was my best concert ever ! The sound was rather better than the first night. They did a great job to fix it. It was like in a dream. Being so close to the stage with a such wonderful sound, it was amazing.There was the same openning part Bap Kennedy and his great guitarist.
</p>
<p>MK and his band arrived on stage at around 9pm maybe a bit earlier can't remember...Then came a special guest Mr Joe Brown, who is a wonderful musician and a funny man as well . They already played together on a tribute to Lonnie Donegan in June 2004. Tonight they performed a song each. A picture of you and Donegan's gone. That was pretty nice to hear DG again. Unfortunately Postcards from Pagraguay was not played. As always, I cryed like a baby during Hill Farmers Blues, his LP sound is so awwww.... oh my god ! just writting it makes my eyes wet... This sound makes me shivers, makes my body shaking my throart hurting ... I'm in love with his sound ! My tears were so visible that Ingrid hug me heheh, thank you my sister !!!
</p>
<p>Standing ovation for Sultans of Swing, and even for Speedway ! In my opinion english are to much shy! I would have run to stage at the end of Speedway, but they quitely waited for the end of Telegraph Road, what we did as well. Ahhh how good it is to stand near the RAH stage, as i said on the 25th may review, the stage is very low without gate. I was a bit less close to MK than the day before but only 2 meters less so it was fine. As I said above, it was my best concert ever and die tonight would not have been a problem with what i saw and heard lol ! Now i have the blues... tour is finished for me, I just have to be patient and keep on dreaming on my memories .
</p>
 


