<h2>London Royal Albert Hall, 2nd Night</h2>
<h3>28th June 2013</h3>

<p>Well I went back to the hotel earlier than expected I was soaked !! I had little rest in a hot bath and then left for the meeting at the Stanhope Arms Pub ! <br/>
There were the same fans as the day before ! After a little drink and food, again we walked to the venue, and fortunately the rain had stopped, thank God !
</p>
<p>We’re a very lucky bunch of guys, because once again we had the very front row (preorders really rock).As for this night I was the purchaser we chose our tickets by drawing lots...I picked up the very same 21 seat as the day before! So What Else!!<br/>
Ruth Moody opened again the show, and I enjoyed the second performance more than her first... Dunno why...</p>

<p>As every night Paul Crockford introduced MK « gooooooood Evening Ladies and Gentlemen please welcome to the Royal Albert Hall, Mister . Mark . Knopfler !»<br/>
Tonight’s audience was less warm than the night before, but the show was awesome though ! Sultans was skipped... (boooh) I kinda missed it, but still very happy with what I heard ! We always expect something and overall I’m always happy with what happens !  I was delighted to hear for the first time on this tour Kingdom of Gold (with Ruth Moody) also very happy to hear the sound of this incredible blue Pensa ! What a beautiful ending solo Mark Played on this tune. This tour is kinda new because every night some songs are switched ! So you don’t really know what’s gonna be skipped or played !  After the encore, we rush to the stage, oh I forgot to mention from last night that  all the seats in the RAH have been changed ! So they are closer to the stage...1 meter approximately !! So no that was not a long rush, but you still must be ready ! The guys from the back arrived very fast and surprised us ! Some stupid ones, pushed us to the stage I was actually bent over 90° half the body lying down the stage !<br/>
Btw this is life...I could enjoy the end of show anyway !<br/>
Another awsome finale with Nigel Hitchcock on a Going Home ! What a night ! What an incredible stay in England!</p>

<P>Back to the Hotel I had a quick snack and drink ! Big hugs with all my lovely friends and went to bed. I had to wake up at 5am to go to Gatwick Airport ! To be at work on time at 2pm....  I was so sad leaving London, I’m a very sensitive person, so again tears came... Looking forward for the next shows ! 5 to go !!../imgs/
</p>
 


