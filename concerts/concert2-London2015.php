<h2>London Royal Albert Hall</h2>


<h3>25th May 2015 </h3> 

<p>Second concert in London, and actually first night at the Royal Albert Hall !</p>

<p>As usually we, fans, had a pre-meeting at the Stanhope Arms pub before the show. Traveling to see Mark is always a good excuse to also meet friends from everywhere in the world and that's a big part of the enjoyement</p>

<p>Show Time,</br>

Ruth Moody & her band were opening the act tonight. I can't say that I don't like her, some tunes are very nice, the band is very talented though, but this is definitly not my cup of tea... 
</p>

<p>Then the "real" show started ! Musicians arrived on stage, and started playing some rythm then Paul Crockford introduced Mark Knopfler, and the show started.</br>
Again Mark was in a very good mood! </br>
The atmosphere was really great tonight ! There were severals standing ovations! It's was kinda electric this evening! Also, there was a real good interaction bewteem Mark and his musicians. Now the band is definitely at its best and you can see/hear it !
</br></br>
I got lucky because my dutch sister AKA Ingrid got me a front row ticket (yes againn, she's definitly the fastest)! The Royal Albert Hall is such an amazing venue, I think from everywhere in the audience you can have a good time, btw I would never ever trade a front row tix for a balcony one, even with champagne & co. The thing I like the most at the RAH is that the stage is very low, there is no security gates (even if the security guys have became "boring"...) so you can easly rush to the stage for the encores...
Ruth Moody came back on stage for a couple of songs, the pity is that they have dropped "Kingdom Of Gold" which was super in the O2...I really miss this duet, and even more the beautifull guitar ending solo.</br></br>
Well talking about tracks, here's the complet setlist of tonight show...( actually no suprise, same as friday with out KOG "sigh")
</p>

<p>
01. Broken Bones</br>
02. Corned Beef City</br>
03. Privateering</br>
04. Father And Son</br>
05. Hill Farmers Blues</br>
06. Skydiver (With Ruth Moody)</br>
07. Laughs And Jokes And Drinks And Smokes</br>
08. Romeo And Juliet (With Nigel)</br>
09. Sultans Of Swing</br>
10. Mighty Man</br>
11. Postcards From Paraguay</br>
12. Marbletown</br>
13. Speedway At Nazareth</br>
14. Telegraph Road</br>
15. So Far Away</br>
16. Wherever I Go (With Ruth Moody)</br>
17. Going Home (With Nigel)</br></br>
</p>
<p>
Tonight the rush time was during the Telegraph Road's final solo, It's the first time in the UK that I see such a rush !!</p>

<p>Then came the end of the super concert and we all went to the pub for a last drink.</p>

<p> 
My last sentence would be to thank all the people involved of making this tour possible for me this year... Sharing room, car-pooling, Xmas presents etc... I won't mention anybody but people will reconize themselves ;)
</p>
 


