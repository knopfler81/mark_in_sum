<h2>Paris Le Zenith</h2>
<h3>3rd June 2015</h3>


<p>So here we are, second night in Le Zenith, I got almost the same seat a little bit closer to Mark!</br>
As we thought the security got renforced with big guys everywhere... We enve were told that the production didn't want us to stand up for the encores, which sounded a little bit crazy.</br>
After Ruth Moody's show we were warned by the security that at 10:30pm they would leave us doing what we want... I think it's the first time I had watched my watch during a concert !! </br></br>
</p>
<p>
As yestrday when Paul Crockford arrived on stage and introduced Mark Knopfler we all stood up and didn't seat untill the end of "Broken Bones" dancing, clapping woowoowing...</br> 
Tonight's atmosphere was lower than yesterday... I suppose because of the big security and also that the concert was not exactly the same. Mark switched a couple of songs like Mighty Man to replace with Haul Away, Shangri-La for So Far Away... And Laughts & jokes wasn't played.</br>
Nearly the end of Marbletown (not sure actually), the security guys left and right after we all rush to the stages. I've tried to take care of my friend Marie and make sure that she's still on the front row, because yesterday someone has stolen her place on the front, and she had to leave as she felt bad in the crow!
</p> </br></br>
<p>Mark made a bad joke, he skipped "Speedway At Nazarreth", I was a little desapointed because it is one of my favorite ! I just hope he won't drop it from the set list for the rest of the tour.
</p>
<p>Btw this evening was not as good as last night, but it's still wonderful to attend any Knopfler gigs! We have good music and good musicians. Mark could never please every single fans, so I totally accept his choises.</p> </br></br>

<p>My last sentence would be to thank all the people involved of making this tour possible for me this year... Sharing room, car-pooling, Xmas presents etc... I won't mention anybody but people will reconize themselves ;) </p>


