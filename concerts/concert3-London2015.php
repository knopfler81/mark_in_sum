<h2>London Royal Albert Hall</h2>
<h3>26th May 2015</h3>

<p>Third concert in London and at the least the last in the UK.</br>
As usual we had a pre-meeting with fans at The Stanhope Arms Pub. A couple of drinks, little food and then we walked again to the RAH !</br>
Again I had a front row ticket! Thanks to Jeroen for having me a good seat!! I had exactly the same seat as the day before. I the middle, a little on the left from Mark !</br>
Ruth Moody opened the act again</br>
Then came Paul Crockford to introduce Mark Knopfler and the show started :-)</br>
Tonight's atomsphere was less warm than the previous night but the band was at his best !  </br></br>
</p>
<p>
Same old set lit, nothing has changed so far... I wonder if they will keep it this way, btw I wish I could hear more songs of the new album which is just a gem.  Anyway I am happy with all ! I got lucky to be there.</p>
<p>
01. Broken Bones</br>
02. Corned Beef City</br>
03. Privateering</br>
04. Father And Son</br>
05. Hill Farmers Blues</br>
06. Skydiver (With Ruth Moody)</br>
07. Laughs And Jokes And Drinks And Smokes</br>
08. Romeo And Juliet (With Nigel)</br>
09. Sultans Of Swing</br>
10. Mighty Man</br>
11. Postcards From Paraguay</br>
12. Marbletown</br>
13. Speedway At Nazareth</br>
14. Telegraph Road</br>
15. So Far Away</br>
16. Wherever I Go (With Ruth Moody)</br>
17. Going Home (With Nigel)</br></br>
</p>


<p>Tonight's rush was a litle later than yesterday (I mean later during the end of TR)but I got surprised by some people rushing faster than me. BTW I managed to be in the very front again. Actually I got stuck between two guys and couldn't move anymore... I was bent in two on the stage almost laying on, and couldn't lift up at all...what an embarrassing position...</br> Mark shook some hands, well when I say "shook" it was not a real shook I should say a quick "check". I got lucky to have a quick check.</p>
<p>
He also gave some picks to other people ! This year Mark interacts much more with the audience than usual.</br></br>
</p>
<p>
My last sentence would be to thank all the people involved of making this tour possible for me this year... Sharing room, car-pooling, Xmas presents etc... I won't mention anybody but people will reconize thelselves ;) ( yes it's gonna be the same sentence for every pages of this tour...).
</p>
 


