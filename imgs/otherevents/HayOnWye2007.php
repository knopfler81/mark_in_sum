<h2>Hay On Wye Festival</h2>


<p>
Sigh* I’ve no pic of that meeting... didn’t dare to ask to the Man, MK was with his family.
<br/><br/>
I landed at BHX airport on Saturday 2nd June 2007. Phil and Pongsi, friends from Switzerland were waiting for me there. That was our first meeting. We rented a car to travel to Wales, destination Hay-On- Wye to see our guitar-hero.<br/><br/>
 The first event we saw was Kitty Aldridge (MK’s wife) talking about her new book “Cryers Hill” and John Walsh about “…”.Mark was in the audience then and quite close to us. There I met other fans like Val and her husband and Martina.<br/>
After that interview I met Mark outside but only for 10 seconds, just enough time to say hi, happy to meet you, and shake hands (always a pleasure to see him in person).<br/><br/>
 Sunday 3rd June the D-Day for MK’s events! The reason why we are here.<br/>
 I met Matt, it’s him who he got me a ticket for the broadcast Hay-On- Sky.<br/>
So I had the luck to attend the recording where Mark was interviewed about his career and played a new song “Secondary Waltz”. Some authors were invited too. The broadcast was shown on British TV (how funny I can see my friends and me, we look really serious).<br/><br/>
 After that event we went to another Mark Knopfler interview but a in a bigger venue ( I heard about 1200s seats).<br/>
This time Mark was interviewed by Matthew Evans. He spoke about his songbook and played few tunes with Guy Fletcher (Guy plays everything, guitar, bass, keyboards):<br/><br/>
 Here is the set list<br/><br/>
Donegan’s gone,<br/>
Secondary Waltz,<br/>
Rüdiger,<br/>
Romeo and Juliet,<br/>
Sailing to Philadelphia,<br/>
Going Home,<br/>
Song for Sonny Liston (encores)<br/><br/>
MK was very cool, pretty funny, and seemed in very good shape.<br/><br/>
 
Monday 4th I came back to France, that was a quite long trip for a tiny week end, but my heart is full of memories. 
</p>
 



        

