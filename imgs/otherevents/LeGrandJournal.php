<h2>Le Grand Journal 25th March 2015</h2>

<p>First of all, a couple of us  made a  naughty little joke, "MK wasn't there , the broadcast is postponded to Monday; we dont know why...., we are So dispointed  blabla" I'll explain how it happened :)
<br/><br/><br/>
We were asked to arrive at 16:00 at the venue, it was very long till the "tv show starts" .
There were tones of teenagers on " school trip", they didn't know who they were about to see... LOL
<br/>
We had to sign paper for the image rights... We were taken our passports... We also had to leave our bags and jackets to a dressing room, one by one and were given a little paper with a number on it you know like in theatre or disco lol.
<br/>
We waited a long time in a "waiting room" (almost one hour)...
Then we arrived on the set and were asked to sit to determintated places... I asked to be nearer to the stage they didn't want lol...
<br/>
When we were all seated the room driver came to explain us all the progress  of broadcast, he explained that on the first part we'll have fun with music and then we would talk about the terrible crash and politics...
<br/>
It's funny to see how it really "works" on a tv show...
<br/>
We had a break between MK's part and the Politic part... we were offred sodas and cake :)
<br/><br/><br/>


We all thought  that MK's broadcast would be aired on the same day after the live tv show with politics & co...
Just before broadcasting, we were told that MK's part would be aired on the next Monday. ( We had no way to comunicate as all our mobile pone were prohibited in the audience.... They were using detector on us before...).
So we though about our french friends who were all ready and waiting with the recorders for MK in their sofa. We imagined them thinking that we went there for nothing so we played the game of the naughty joke, till this morning... Well enough said about this...
<br/>
MK was there indeed and in very good shape ! :) he was with Guy, Ianto, Dave Bronze, Robbie McIntosh !( Paul Crockford and Glen Stagger too).
<br/>
Well about MK's interview, in the audience it was very hard to hear really.... Sound was low....  And the french translation covered MK's voice so it was a little messy with the two voices at the same time....
<br/>
So the things I could understand... are almost here... 
<br/>
At the same time a french actress was interviewed (Lea Seydoux)... She's gonna be the next "James Bond Girl".
<br/>
Antoine De Caunne (the presenter) asked MK " Would you, if you were asked, write the Next James Bond Soundtrack..." Of course the answer didn't surprised me....
He said, no, movie music is not his "stuff" (well he did some pretty good before didnt he?? uhh??)
<br/>
He also talked about is guitar skills, his way to hold the neck of the guitar like a plumber hold a pipe... (lol He always say this one...)
He said that he is an selft-educated guitar player.... and his band are REAL Musicians.... You know MK's always so humble on his skills...
They showed some old DS video from the early years until OES... Then they talked about Tracker... ( not sure I remembre what they really said on it...).(remeber sound in the audience was bad....)
<br/><br/>
Then he went on the stage which was just on my right, So I was pretty close 5 meters or something..., and the view was good ! Sound also was good ( we had the stage's sound !).
<br/><br/>
<br/>
MK played Beryl and also......<br/>
