<h2>Le Mans 2006 </h2>



<p>
After a long and tiering trip I arrived in Le Mans to support and try to meet my giutar hero…. <br/>You should guess who…Mark Knopfler indeed!!
 Very early on Friday morning I went to the tickets office, to buy my dear tickets with which I could get in Mark’s paddock. 
After going round for a while admiring the wonderful old cars I discovered MK’s beautiful Austin Healey from 1954 N°36.
<br/><br/>
After a nice walk I decided to leave the paddock, and there I saw Mark on his red scooter! Oh Yes! That’s him for sure!!! Mark was in a hurry no time to catch him…<br/><br/>
The time went… no Mark on the horizon he is quite… discrete…<br/>
It is just before going to bed that I finally met him! WOW I didn’t believe it anymore!<br/>
I nearly had all I wanted! An autograph on my Strat back’s cover, kisses and a sweet smile…<br/>
The night was short, cold and noisy… yeah 24 hours of races! Hard to fall asleep.<br/>
The day after in the afternoon, I had the chance to see him again… but he was surrounded by other fans and didn’t had time to make a photo with me, but to apologize himself he blowed me a kiss. So I left charmed.
</p>
 


